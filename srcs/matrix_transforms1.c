#include "fdf.h"

void	make_translation_mat(t_mat44f *m, t_vec3f *v)
{
	m->m00 = 1;
	m->m01 = 0;
	m->m02 = 0;
	m->m03 = 0;
	m->m10 = 0;
	m->m11 = 1;
	m->m12 = 0;
	m->m13 = 0;
	m->m20 = 0;
	m->m21 = 0;
	m->m22 = 1;
	m->m23 = 0;
	m->m30 = v->x;
	m->m31 = v->y;
	m->m32 = v->z;
	m->m33 = 1;
}

void	identity_mat(t_mat44f *m)
{
	m->m00 = 1;
	m->m01 = 0;
	m->m02 = 0;
	m->m03 = 0;
	m->m10 = 0;
	m->m11 = 1;
	m->m12 = 0;
	m->m13 = 0;
	m->m20 = 0;
	m->m21 = 0;
	m->m22 = 1;
	m->m23 = 0;
	m->m30 = 0;
	m->m31 = 0;
	m->m32 = 0;
	m->m33 = 1;
}

void	unif_scale_mat(t_mat44f *m, float s)
{
	m->m00 = s;
	m->m01 = 0;
	m->m02 = 0;
	m->m03 = 0;
	m->m10 = 0;
	m->m11 = s;
	m->m12 = 0;
	m->m13 = 0;
	m->m20 = 0;
	m->m21 = 0;
	m->m22 = s;
	m->m23 = 0;
	m->m30 = 0;
	m->m31 = 0;
	m->m32 = 0;
	m->m33 = 1;
}

void	non_unif_scale_mat(t_mat44f *m, t_vec3f *v)
{
	m->m00 = v->x;
	m->m01 = 0;
	m->m02 = 0;
	m->m03 = 0;
	m->m10 = 0;
	m->m11 = v->y;
	m->m12 = 0;
	m->m13 = 0;
	m->m20 = 0;
	m->m21 = 0;
	m->m22 = v->z;
	m->m23 = 0;
	m->m30 = 0;
	m->m31 = 0;
	m->m32 = 0;
	m->m33 = 1;
}
